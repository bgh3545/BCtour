package com.greenart.ch1;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.ResultSet;
import java.sql.Statement;

public class DBConnectionTest343 {

	public static void main(String[] args) throws Exception {

		// 스키마의 이름(db이름)이 다른 경우 알맞게 변경해야 함
		String DB_URL = "jdbc:mysql://localhost:3306/google_db?useUnicode=true&characterEncoding=utf8";
		
		// DB의 userid와 pwd를 알맞게 변경해야 함
		String DB_USER = "root";
		String DB_PASSWORD = "0000";
		
		Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD); // 데이터베이스의 연결을 얻는다.
		Statement stmt = conn.createStatement(); // Statement를 생성한다.
		
		String query = "SELECT * FROM member"; // 시스템의 현재 날짜시간을 출력하는 쿼리(query)
		ResultSet rs = stmt.executeQuery(query); // query를 실행한 결과를 rs에 담는다.
		
		// 실행결과가 담긴 rs에서 한 줄씩 일겅서 출력
		while(rs.next()) {
			String curDate = rs.getString(1); // 읽어온 행의 첫번째 칼럼의 값을 String으로 읽어서 curDate에 저장
			System.out.println(curDate);
		}
	}
}